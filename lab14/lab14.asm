AND R2,R2, #0 ;Clears R2
AND R3,R3, #0 ;Clears R3
ADD R3,R3, #8 ;Initalizes line count to 16
ADD R3,R3, #8; R3=16
AND R4,R4, #0 ;Clears R4


;this is our counter
