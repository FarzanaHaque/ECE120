BRn One
LDI R0, ZeroChar
OUT
BRnzp SKIP
One LDI R0, OneChar
OUT
SKIP ADD R6, R6, R6
ADD R4, R4,#-1
BRp BITLOOP
ADD R1, R1, #1;
BRnzp NEWCHAR
;NEXT CHARACTER

nullcharacter ;LDI R0, ZeroChar
;OUT
