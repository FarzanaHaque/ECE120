ADD R2, R2, R2;
ADD R2, R2, R2;
ADD R2, R2, R2 ; r2 is multiplied by 16
LEA R5,FONT_DATA ;font data is loaded
ADD R2, R2, R5;r2 is the current location to read
; yes but it won't work ADD R2, R2, R7; IS THIS THE THING WE NEED?
